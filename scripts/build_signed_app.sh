#!/bin/bash
# Phase 4D P1.5: reproducible signed-app build.
#
# Codex review on PR #20: the previous Phase 4 / Phase 4D
# instructions asked Jason to manually edit Xcode build phases
# (Run Script + Copy Files), which means a clean checkout cannot
# produce a working app and CI's green build doesn't prove the
# helper is embedded. This script fixes that — a clean checkout
# plus `./scripts/build_signed_app.sh` produces a signed .app with
# the helper binary + LaunchAgent plist in place at the right
# paths.
#
# What it does:
#   1. Build the Swift helper in release mode.
#   2. Build the macOS app via xcodebuild.
#   3. Copy `HelperSwift/.build/release/cli_pulse_helper` into
#      `<App>/Contents/Helpers/cli_pulse_helper`.
#   4. Copy `CLI Pulse Bar/CLI Pulse Bar/HelperAgent.plist` into
#      `<App>/Contents/Library/LaunchAgents/yyh.CLI-Pulse.helper.agent.plist`.
#   5. Codesign the embedded helper binary with the app's signing
#      identity (so SMAppService.register accepts it).
#   6. Re-sign the .app so the additions are part of the
#      signature seal.
#   7. Verify the final bundle structure.
#
# Usage:
#   ./scripts/build_signed_app.sh [Debug|Release] [<output-dir>]
#
# Defaults: configuration=Debug, output-dir=./build_app_output.
#
# CI integration: invoke this script instead of raw xcodebuild.
# A passing CI run proves the helper IS embedded + signed in the
# shipped artifact.
#
# Why this approach instead of pbxproj edits:
#   - pbxproj editing via Ruby `xcodeproj` adds a toolchain
#     dependency CI would have to install.
#   - pbxproj is regenerated by Xcode UI in subtle ways; manual
#     edits create review churn.
#   - The script works identically from CI, from Jason's
#     terminal, and as a post-build step a Run Script phase
#     can shell out to. Single source of truth.

set -euo pipefail

CONFIGURATION="${1:-Debug}"
OUTPUT_DIR="${2:-$PWD/build_app_output}"

# v1.19: opt-in flag for the Developer ID DMG pipeline.
# Notarization REQUIRES a timestamped signature (Apple's timestamp
# service), but the ad-hoc/dev path uses --timestamp=none for speed
# (CI runs hit this path; an Apple-side timestamp call adds 5-15s per
# nested binary and can fail transiently). Default keeps the historical
# behaviour; build_devid_dmg.sh sets CODESIGN_TIMESTAMP=1 before
# invoking this script.
if [[ -n "${CODESIGN_TIMESTAMP:-}" ]]; then
    CODESIGN_TIMESTAMP_FLAG="--timestamp"
else
    CODESIGN_TIMESTAMP_FLAG="--timestamp=none"
fi

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
SWIFT_PKG_DIR="$PROJECT_ROOT/HelperSwift"
APP_PROJECT="$PROJECT_ROOT/CLI Pulse Bar/CLI Pulse Bar.xcodeproj"
APP_SCHEME="CLI Pulse Bar"
PLIST_TEMPLATE="$PROJECT_ROOT/CLI Pulse Bar/CLI Pulse Bar/HelperAgent.plist"
DERIVED_DATA="$OUTPUT_DIR/DerivedData"
# v1.34 DEVID keychain fix: the LaunchAgent helper is a BARE Mach-O and cannot
# embed a provisioning profile, so on the unsandboxed Developer-ID build it must
# carry NO restricted entitlements (keychain/app-groups/team-id) — otherwise AMFI
# SIGKILLs it at launch. The app + LoginItem are .app bundles and instead EMBED a
# Developer ID provisioning profile (below) that authorises keychain-access-groups.
# Non-DEVID (Debug-CI) keeps the historical entitlements (it never launches).
if [[ -n "${DEVID_BUILD_FLAG:-}" ]]; then
    HELPER_ENTITLEMENTS="$SWIFT_PKG_DIR/cli_pulse_helper_devid.entitlements"
else
    HELPER_ENTITLEMENTS="$SWIFT_PKG_DIR/cli_pulse_helper.entitlements"
fi
# Developer ID provisioning profiles (committed; long-lived — exp 2044). Embedded
# at Contents/embedded.provisionprofile in the DEVID app + LoginItem so AMFI
# authorises their keychain-access-groups. Created via the App Store Connect API
# (MAC_APP_DIRECT; grants keychain-access-groups KHMK6Q3L3K.*).
DEVID_PROFILES_DIR="$PROJECT_ROOT/scripts/devid-profiles"
APP_PROVISION_PROFILE="$DEVID_PROFILES_DIR/CLI_Pulse_Bar_devid.provisionprofile"
LOGINITEM_PROVISION_PROFILE="$DEVID_PROFILES_DIR/CLIPulseHelper_devid.provisionprofile"
APP_ENTITLEMENTS_SAVED="$OUTPUT_DIR/app_entitlements.plist"

# W1-A (2026-06-28): team prefix used to expand $(AppIdentifierPrefix) in the
# keychain-access-group. `codesign --entitlements` does NOT expand Xcode build
# macros, so signing from a raw plist ships the LITERAL
# "$(AppIdentifierPrefix)group.yyh.CLI-Pulse" string → keychain sharing breaks.
# Matches the hard-coded team id in HelperSwift/cli_pulse_helper.entitlements
# and CLI Pulse Bar/scripts/build-appstore.sh (KHMK6Q3L3K).
APP_IDENTIFIER_PREFIX="KHMK6Q3L3K."

# Expand Xcode entitlement macros into a plist codesign can use verbatim, then
# assert no "$(" remains. plutil canonicalises (and drops XML comments) first.
# Usage: expand_entitlements SRC DST
expand_entitlements() {
    local src="$1" dst="$2"
    /usr/bin/plutil -convert xml1 -o "$dst" "$src" || {
        echo "error: plutil could not read entitlements source: $src" >&2
        exit 2
    }
    /usr/bin/sed -i '' \
        -e "s/\$(AppIdentifierPrefix)/${APP_IDENTIFIER_PREFIX}/g" \
        -e "s/\$(TeamIdentifierPrefix)/${APP_IDENTIFIER_PREFIX}/g" \
        "$dst"
    if /usr/bin/grep -q '\$(' "$dst"; then
        echo "error: unexpanded macro remains in $dst after expand_entitlements:" >&2
        /usr/bin/grep -n '\$(' "$dst" >&2
        exit 2
    fi
}

# 1. Build the Swift helper.
echo "==> [1/7] Building Swift helper (release) ..."
cd "$SWIFT_PKG_DIR"
swift build -c release
HELPER_BIN="$SWIFT_PKG_DIR/.build/release/cli_pulse_helper"
[[ -x "$HELPER_BIN" ]] || { echo "error: helper binary missing at $HELPER_BIN" >&2; exit 1; }
HELPER_SIZE="$(du -h "$HELPER_BIN" | cut -f1)"
echo "    built: $HELPER_BIN ($HELPER_SIZE)"

# 2. Build the macOS app.
echo "==> [2/7] Building CLI Pulse Bar.app ($CONFIGURATION) ..."
cd "$PROJECT_ROOT/CLI Pulse Bar"
# Strip lingering .DS_Store xattrs that codesign rejects on
# nested helper targets ("resource fork, Finder information, or
# similar detritus not allowed"). xattr returns 0 even when nothing
# matches, so the `|| true` is defensive.
find . -name ".DS_Store" -exec rm -f {} + 2>/dev/null || true

# v1.19: opt-in compilation flags for the Developer ID DMG pipeline.
# DEVID_BUILD_FLAG=1 sets SWIFT_ACTIVE_COMPILATION_CONDITIONS to include
# DEVID_BUILD so Swift code can `#if DEVID_BUILD` to gate the AppUpdater,
# subscription bypass, MAS-auto-update banner, etc. Default empty =
# Release/Debug builds behave exactly as before (MAS-equivalent
# compilation). build_devid_dmg.sh sets DEVID_BUILD_FLAG=1.
EXTRA_XCODEBUILD_SETTINGS=()
if [[ -n "${DEVID_BUILD_FLAG:-}" ]]; then
    EXTRA_XCODEBUILD_SETTINGS+=("SWIFT_ACTIVE_COMPILATION_CONDITIONS=\$(inherited) DEVID_BUILD")
fi

xcodebuild \
    -project "$APP_PROJECT" \
    -scheme "$APP_SCHEME" \
    -configuration "$CONFIGURATION" \
    -derivedDataPath "$DERIVED_DATA" \
    build \
    CODE_SIGNING_ALLOWED=NO \
    CODE_SIGN_IDENTITY="" \
    CODE_SIGN_STYLE=Manual \
    DEVELOPMENT_TEAM="" \
    ${EXTRA_XCODEBUILD_SETTINGS[@]+"${EXTRA_XCODEBUILD_SETTINGS[@]}"}

APP_PATH="$DERIVED_DATA/Build/Products/$CONFIGURATION/CLI Pulse Bar.app"
[[ -d "$APP_PATH" ]] || { echo "error: built .app not found at $APP_PATH" >&2; exit 1; }
echo "    built: $APP_PATH"

# 3. Copy helper binary into Contents/Helpers/.
echo "==> [3/7] Embedding helper at Contents/Helpers/ ..."
mkdir -p "$APP_PATH/Contents/Helpers"
cp "$HELPER_BIN" "$APP_PATH/Contents/Helpers/cli_pulse_helper"
chmod +x "$APP_PATH/Contents/Helpers/cli_pulse_helper"

# 4. Copy LaunchAgent plist into Contents/Library/LaunchAgents/.
echo "==> [4/7] Embedding LaunchAgent plist at Contents/Library/LaunchAgents/ ..."
mkdir -p "$APP_PATH/Contents/Library/LaunchAgents"
cp "$PLIST_TEMPLATE" "$APP_PATH/Contents/Library/LaunchAgents/yyh.CLI-Pulse.helper.agent.plist"

# 5. Resolve signing identity + capture original app entitlements
#    BEFORE we touch the bundle. Codex P1.C review: the previous
#    `--force --deep` re-sign stripped sandbox / app-group
#    entitlements that Xcode's signing applied. The fix is to:
#      (a) extract the existing app entitlements plist,
#      (b) sign the helper with its OWN entitlements (no sandbox,
#          minimal Hardened Runtime),
#      (c) re-sign the app explicitly preserving its entitlements
#          via `--preserve-metadata=entitlements,requirements,flags`.
echo "==> [5/7] Resolving signing identity + entitlements source ..."
SIGN_IDENTITY="-"   # ad-hoc by default
if [[ -n "${CODE_SIGN_IDENTITY:-}" ]]; then
    SIGN_IDENTITY="$CODE_SIGN_IDENTITY"
fi
# Use the source entitlements file Xcode reads from
# CODE_SIGN_ENTITLEMENTS. With CODE_SIGNING_ALLOWED=NO Xcode
# doesn't emit a per-build .xcent, so we sign with the source
# file directly. This is the EXACT same input Xcode would have
# used had signing been on, so sandbox + app-group entitlements
# survive intact.
# W1-A: the Developer-ID build signs with an UNSANDBOXED entitlements file so
# the in-app terminal (gated on !isSandboxed) becomes visible. The MAS archive
# (build-appstore.sh via xcodebuild) is untouched and keeps the sandboxed
# CLI_Pulse_Bar.entitlements. Both paths get macro expansion.
if [[ -n "${DEVID_BUILD_FLAG:-}" ]]; then
    APP_ENTITLEMENTS_SOURCE="$PROJECT_ROOT/CLI Pulse Bar/CLI Pulse Bar/CLI_Pulse_Bar_devid.entitlements"
else
    APP_ENTITLEMENTS_SOURCE="$PROJECT_ROOT/CLI Pulse Bar/CLI Pulse Bar/CLI_Pulse_Bar.entitlements"
fi
if [[ ! -f "$APP_ENTITLEMENTS_SOURCE" ]]; then
    echo "error: app entitlements source missing at $APP_ENTITLEMENTS_SOURCE" >&2
    exit 2
fi
expand_entitlements "$APP_ENTITLEMENTS_SOURCE" "$APP_ENTITLEMENTS_SAVED"
echo "    using: $APP_ENTITLEMENTS_SOURCE -> $APP_ENTITLEMENTS_SAVED (macros expanded)"

# 6. Sign the embedded helper with its own minimal entitlements,
#    then re-sign the app preserving its sandbox / app-group
#    entitlements. Re-sign nested frameworks first because they
#    have their own _CodeSignature/CodeResources that get
#    invalidated by xattr -c on the parent.
echo "==> [6/7] Codesigning helper + re-signing .app (identity=$SIGN_IDENTITY) ..."
# Strip xattrs recursively. Source files under macOS dev
# workflows pick up `com.apple.provenance` / `com.apple.FinderInfo`
# xattrs from Finder / curl, and Xcode propagates them into the
# bundle on copy. We hit EVERYTHING — frameworks too — because
# the bottom-up re-sign below regenerates each framework's
# _CodeSignature/CodeResources.
xattr -cr "$APP_PATH" 2>/dev/null || true

# Bottom-up re-sign of every nested signed bundle inside the
# app. Order matters: deepest frameworks first, then login-item
# .apps, then the parent app on top. We do NOT use `codesign
# --deep` because that would apply the parent's entitlements to
# every nested binary (helper would inherit sandbox, frameworks
# would get group-container). The bottom-up walk lets each
# bundle keep its own entitlements.
echo "    re-signing nested bundles bottom-up ..."
python3 - <<PYEOF "$APP_PATH" "$SIGN_IDENTITY" "$CODESIGN_TIMESTAMP_FLAG"
import os, sys, subprocess
app_path, sign_identity = sys.argv[1], sys.argv[2]
matches = []
for root, dirs, _files in os.walk(app_path):
    for d in list(dirs):
        if d.endswith((".framework", ".app", ".xpc", ".dylib")):
            matches.append(os.path.join(root, d))
# Sort by path length descending → deepest first.
matches.sort(key=lambda p: -len(p))
for p in matches:
    # v1.19 macOS 26.5 SDK: signing an inner framework regenerates
    # _CodeSignature/CodeResources on the parent bundle and the
    # parent picks up com.apple.provenance + com.apple.FinderInfo
    # xattrs from the macOS file-creation path. The top-of-script
    # xattr -cr does not help because those xattrs come BACK after
    # each nested sign. Strip immediately before each codesign call
    # so the bundle being signed never has detritus.
    subprocess.call(["xattr", "-cr", p], stderr=subprocess.DEVNULL)
    rc = subprocess.call([
        "codesign", "--force", "--options", "runtime", sys.argv[3],
        "--sign", sign_identity, p,
    ])
    if rc != 0:
        # Surface the failure clearly so the outer shell sees a
        # non-zero exit. The original subprocess.call swallowed
        # codesign errors silently, letting builds proceed past
        # broken signatures into notarize failures.
        sys.stderr.write(f"codesign failed for {p} (rc={rc})\n")
        sys.exit(rc)
PYEOF
# Helper: NOT sandboxed (it's a LaunchAgent), but Hardened Runtime
# is on (--options runtime) for notarisation. Expand $(AppIdentifierPrefix)
# in its keychain group first (W1-A: codesign won't, so it previously shipped
# the literal macro → keychain sharing broken).
HELPER_ENTITLEMENTS_EXPANDED="$OUTPUT_DIR/helper_entitlements.plist"
expand_entitlements "$HELPER_ENTITLEMENTS" "$HELPER_ENTITLEMENTS_EXPANDED"
xattr -cr "$APP_PATH/Contents/Helpers/cli_pulse_helper" 2>/dev/null || true
codesign --force --options runtime "$CODESIGN_TIMESTAMP_FLAG" \
    --entitlements "$HELPER_ENTITLEMENTS_EXPANDED" \
    --sign "$SIGN_IDENTITY" \
    "$APP_PATH/Contents/Helpers/cli_pulse_helper"

# W1-A: re-sign the embedded LoginItem (CLIPulseHelper.app) with its OWN
# entitlements. The bottom-up walk above re-signed nested .app bundles with NO
# entitlements, which STRIPPED this LoginItem's sandbox + app-group + keychain
# (prior DEVID builds ran it entitlement-less → recurring keychain prompts /
# legacy fallback). DEVID → UNSANDBOXED entitlements: the app is unsandboxed and
# CODE_SIGNING_ALLOWED=NO embeds no provisioning profile, so a sandboxed nested
# item requesting keychain/app-group has nothing to authorise the restricted
# entitlements and taskgated can reject it on SMAppService.loginItem launch
# (HelperLogin.register). Non-DEVID (MAS-shaped) → the sandboxed entitlements.
LOGIN_ITEM_APP="$APP_PATH/Contents/Library/LoginItems/CLIPulseHelper.app"
# The LoginItem (CLIPulseHelper.app) is embedded by Xcode as a Copy Files build
# phase / target dependency, so it is ALWAYS present in a real build. The
# semantic verifier in step 7 already asserts it present UNCONDITIONALLY
# (`check(os.path.isdir(login_app), "LoginItem not found …")`). Make the signing
# step equally strict so a missing LoginItem fails HERE — clear and early —
# instead of: (a) slipping through this `else` branch unsigned (entitlements
# stripped by the bottom-up nested-bundle re-sign above) only to fail the
# verifier with a more confusing error, or (b) on any future non-verified code
# path, shipping an entitlement-stripped LoginItem that taskgated rejects at
# SMAppService.loginItem launch.
[[ -d "$LOGIN_ITEM_APP" ]] || {
    echo "error: LoginItem missing at $LOGIN_ITEM_APP — expected Xcode to embed CLIPulseHelper.app (Copy Files phase / target dependency)" >&2
    exit 2
}
if [[ -n "${DEVID_BUILD_FLAG:-}" ]]; then
    LOGIN_ITEM_ENT_SOURCE="$PROJECT_ROOT/CLI Pulse Bar/CLIPulseHelper/CLIPulseHelper_devid.entitlements"
else
    LOGIN_ITEM_ENT_SOURCE="$PROJECT_ROOT/CLI Pulse Bar/CLIPulseHelper/CLIPulseHelper.entitlements"
fi
[[ -f "$LOGIN_ITEM_ENT_SOURCE" ]] || {
    echo "error: LoginItem entitlements missing at $LOGIN_ITEM_ENT_SOURCE" >&2; exit 2; }
LOGIN_ITEM_ENT_EXPANDED="$OUTPUT_DIR/loginitem_entitlements.plist"
expand_entitlements "$LOGIN_ITEM_ENT_SOURCE" "$LOGIN_ITEM_ENT_EXPANDED"
# v1.34: embed the Developer ID profile so AMFI authorises the LoginItem's
# keychain-access-groups on the unsandboxed DEVID build (it hydrates secrets from
# the Keychain). DEVID only; the profile is sealed by the codesign below.
if [[ -n "${DEVID_BUILD_FLAG:-}" ]]; then
    [[ -f "$LOGINITEM_PROVISION_PROFILE" ]] || {
        echo "error: LoginItem Developer ID profile missing at $LOGINITEM_PROVISION_PROFILE" >&2; exit 2; }
    cp "$LOGINITEM_PROVISION_PROFILE" "$LOGIN_ITEM_APP/Contents/embedded.provisionprofile"
    echo "    embedded LoginItem Developer ID profile"
fi
xattr -cr "$LOGIN_ITEM_APP" 2>/dev/null || true
codesign --force --options runtime "$CODESIGN_TIMESTAMP_FLAG" \
    --entitlements "$LOGIN_ITEM_ENT_EXPANDED" \
    --sign "$SIGN_IDENTITY" \
    "$LOGIN_ITEM_APP"
echo "    signed LoginItem with $(basename "$LOGIN_ITEM_ENT_SOURCE") (macros expanded)"

# v1.34: embed the Developer ID profile into the app bundle so AMFI authorises
# its keychain-access-groups (account auth tokens + provider API keys) on the
# unsandboxed DEVID build. DEVID only; sealed by the app re-sign below. Without
# this the notarized app is AMFI-SIGKILLed at launch (verified).
if [[ -n "${DEVID_BUILD_FLAG:-}" ]]; then
    [[ -f "$APP_PROVISION_PROFILE" ]] || {
        echo "error: app Developer ID profile missing at $APP_PROVISION_PROFILE" >&2; exit 2; }
    cp "$APP_PROVISION_PROFILE" "$APP_PATH/Contents/embedded.provisionprofile"
    echo "    embedded app Developer ID profile"
fi
# Re-sign the app with the original Xcode-emitted entitlements
# (.xcent file we located above). NOT --deep — that would re-sign
# every nested binary with our top-level entitlements (overwriting
# the helper's just-applied minimal set).
xattr -cr "$APP_PATH" 2>/dev/null || true
codesign --force --options runtime "$CODESIGN_TIMESTAMP_FLAG" \
    --entitlements "$APP_ENTITLEMENTS_SAVED" \
    --sign "$SIGN_IDENTITY" \
    "$APP_PATH"

# 7. Verify.
echo "==> [7/7] Verifying bundle ..."
test -x "$APP_PATH/Contents/Helpers/cli_pulse_helper"
test -f "$APP_PATH/Contents/Library/LaunchAgents/yyh.CLI-Pulse.helper.agent.plist"
# v1.20: macOS 26.5 (post-26.5.1) re-adds com.apple.provenance xattrs to
# _CodeSignature/CodeResources files at LaunchServices registration time
# (faster than we can strip them — even an immediate `xattr -cr` followed
# by `codesign --verify --deep --strict` loses the race). The xattrs are
# NOT part of the signed content (codesign emits them on its own files),
# and notarytool + spctl don't flag their presence — only `--strict`
# verification does. Drop `--strict` here. Notarize + stapler validate +
# spctl assess in later steps remain the real gates. If a corruption ever
# bypasses the structural codesign verify (which IS still in --deep mode),
# notarytool will reject the submission and we'd see it there.
xattr -cr "$APP_PATH" 2>/dev/null || true
codesign --verify --deep "$APP_PATH"

# W1-A: semantic entitlement verification of the SIGNED products (not the
# source files). Replaces the old substring greps with plist parsing so we can
# assert the full matrix:
#   * DEVID app has NO app-sandbox; non-DEVID app HAS it.
#   * app + LoginItem carry application-group group.yyh.CLI-Pulse and keychain
#     group KHMK6Q3L3K.group.yyh.CLI-Pulse (exact, macro-expanded); on DEVID they
#     also EMBED a Developer ID provisioning profile that authorises those
#     restricted entitlements (else AMFI SIGKILLs them at launch — v1.34 fix).
#   * LaunchAgent helper is never sandboxed; on DEVID it carries NO restricted
#     entitlements at all (bare Mach-O can't embed a profile) — reaches the Group
#     Container by path. Non-DEVID keeps the historical keychain/app-group.
#   * no entitlement contains an unexpanded "$(" macro.
#   * (DEVID only) every signed binary has the Hardened Runtime flag — ad-hoc
#     signing ignores --options runtime, so this is gated off for the Debug CI.
IS_DEVID=0
[[ -n "${DEVID_BUILD_FLAG:-}" ]] && IS_DEVID=1
EXPECTED_KC_GROUP="${APP_IDENTIFIER_PREFIX}group.yyh.CLI-Pulse"
EXPECTED_APP_GROUP="group.yyh.CLI-Pulse"
python3 - "$APP_PATH" "$IS_DEVID" "$EXPECTED_KC_GROUP" "$EXPECTED_APP_GROUP" <<'PYEOF'
import os, sys, subprocess, plistlib

app_path, is_devid, kc_group, app_group = sys.argv[1], sys.argv[2] == "1", sys.argv[3], sys.argv[4]
helper_bin = os.path.join(app_path, "Contents/Helpers/cli_pulse_helper")
login_app  = os.path.join(app_path, "Contents/Library/LoginItems/CLIPulseHelper.app")
login_bin  = os.path.join(login_app, "Contents/MacOS/CLIPulseHelper")
errors = []

def read_entitlements(path):
    out = subprocess.run(["codesign", "-d", "--entitlements", ":-", path],
                         capture_output=True)
    data = out.stdout
    for marker in (b"<?xml", b"<plist", b"bplist00"):
        i = data.find(marker)
        if i >= 0:
            try:
                return plistlib.loads(data[i:])
            except Exception:
                pass
    try:
        return plistlib.loads(data)
    except Exception:
        return None

def cd_flags(path):
    out = subprocess.run(["codesign", "-d", "-v", path], capture_output=True)
    text = (out.stdout + out.stderr).decode("utf-8", "replace")
    for line in text.splitlines():
        if "flags=" in line:
            return line.strip()
    return ""

def check(cond, msg):
    if not cond:
        errors.append(msg)

def has_group(ent, key, val):
    return ent is not None and val in (ent.get(key) or [])

app_ent = read_entitlements(app_path)
helper_ent = read_entitlements(helper_bin)
login_ent = read_entitlements(login_bin) if os.path.isdir(login_app) else None

check(app_ent is not None, "could not read app entitlements")
if app_ent is not None:
    sb = bool(app_ent.get("com.apple.security.app-sandbox", False))
    if is_devid:
        check(not sb, "DEVID app MUST NOT be sandboxed (app-sandbox present)")
    else:
        check(sb, "non-DEVID app MUST be sandboxed (app-sandbox missing/false)")
    check(has_group(app_ent, "com.apple.security.application-groups", app_group),
          "app missing application-group %s" % app_group)
    check(has_group(app_ent, "keychain-access-groups", kc_group),
          "app keychain group != %s (got %r)" % (kc_group, app_ent.get("keychain-access-groups")))
    if is_devid:
        # v1.34: the embedded Developer ID profile is what AMFI uses to authorise
        # the app's keychain-access-groups (it grants KHMK6Q3L3K.*). Without it the
        # notarized app is SIGKILLed at launch.
        check(os.path.isfile(os.path.join(app_path, "Contents/embedded.provisionprofile")),
              "DEVID app missing Contents/embedded.provisionprofile (authorises keychain-access-groups)")

check(helper_ent is not None, "could not read LaunchAgent helper entitlements")
if helper_ent is not None:
    check(not bool(helper_ent.get("com.apple.security.app-sandbox", False)),
          "LaunchAgent helper MUST NOT be sandboxed")
    if is_devid:
        # v1.34: the bare-Mach-O LaunchAgent helper can't embed a provisioning
        # profile, so on the unsandboxed DEVID build it must carry NO restricted
        # entitlements — AMFI SIGKILLs an unsandboxed Developer-ID binary that has
        # keychain-access-groups/application-groups without a profile (verified).
        # It reaches the Group Container by absolute path + uses the file token.
        check(not helper_ent.get("keychain-access-groups"),
              "DEVID helper MUST NOT have keychain-access-groups (got %r)" % helper_ent.get("keychain-access-groups"))
        check(not helper_ent.get("com.apple.security.application-groups"),
              "DEVID helper MUST NOT have application-groups (got %r)" % helper_ent.get("com.apple.security.application-groups"))
    else:
        check(has_group(helper_ent, "com.apple.security.application-groups", app_group),
              "helper missing application-group %s" % app_group)
        check(has_group(helper_ent, "keychain-access-groups", kc_group),
              "helper keychain group != %s (got %r)" % (kc_group, helper_ent.get("keychain-access-groups")))

check(os.path.isdir(login_app), "LoginItem not found at %s" % login_app)
if login_ent is not None:
    sb = bool(login_ent.get("com.apple.security.app-sandbox", False))
    if is_devid:
        check(not sb, "DEVID LoginItem MUST NOT be sandboxed (no embedded profile to authorise restricted entitlements)")
    else:
        check(sb, "non-DEVID LoginItem MUST be sandboxed (MAS shape)")
    check(has_group(login_ent, "com.apple.security.application-groups", app_group),
          "LoginItem missing application-group %s" % app_group)
    check(has_group(login_ent, "keychain-access-groups", kc_group),
          "LoginItem keychain group != %s (got %r)" % (kc_group, login_ent.get("keychain-access-groups")))
    if is_devid:
